            </li>
            <li>
                For event tickets: Event ticket purchases are non-refundable unless the event is canceled or rescheduled. In the event of a cancellation or rescheduling, we will provide information regarding the refund process and timelines.
            </li>
        </ul>
    </dd>

    <dt class="fw-bold text-secondary">Contact Us:</dt>
