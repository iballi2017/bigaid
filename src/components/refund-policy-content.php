<h5 class="fs-250 fw-bold mb-4 text-capitalize text-primary">Refund Policy</h5>
<p>This Refund Policy outlines the guidelines and procedures for refund requests related to donations, Sponsorships and purchases/Subscriptions made through BIGAiD Foundation's platforms. Please read this policy carefully.</p>

<br />
<dl>
    <dt class="fw-bold text-secondary">Donations:</dt>
    <dd>
        <p class="mb-3">
            Donations/ Sponsorships made to BIGAiD Foundation are considered final and non-refundable. Once a donation is made, it cannot be reversed. We deeply appreciate your generosity and support, and we are committed to using your contributions effectively to advance our mission in the healthcare and wellness sectors. Therefore, we do not entertain refund requests for donations.
        </p>
    </dd>

    <dt class="fw-bold text-secondary">Purchases:</dt>
    <dd>
        <p class="mb-3">
